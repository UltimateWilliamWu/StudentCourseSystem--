package Pojo;

public class SelectedCourses {
    int cid;
    String cname,csemester,ccredits,tname;

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }

    public String getCname() {
        return cname;
    }

    public void setCname(String cname) {
        this.cname = cname;
    }

    public String getCsemester() {
        return csemester;
    }

    public void setCsemester(String csemester) {
        this.csemester = csemester;
    }

    public String getCcredits() {
        return ccredits;
    }

    public void setCcredits(String ccredits) {
        this.ccredits = ccredits;
    }

    public String getTname() {
        return tname;
    }

    public void setTname(String tname) {
        this.tname = tname;
    }
}
