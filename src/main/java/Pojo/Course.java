package Pojo;

public class Course {
    int cid,tid,ccredits;
    String cname,csemester,cyear,ccourseday,ccoursetime;

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }

    public int getTid() {
        return tid;
    }

    public void setTid(int tid) {
        this.tid = tid;
    }

    public int getCcredits() {
        return ccredits;
    }

    public void setCcredits(int ccredits) {
        this.ccredits = ccredits;
    }

    public String getCname() {
        return cname;
    }

    public void setCname(String cname) {
        this.cname = cname;
    }

    public String getCsemester() {
        return csemester;
    }

    public void setCsemester(String csemester) {
        this.csemester = csemester;
    }

    public String getCyear() {
        return cyear;
    }

    public void setCyear(String cyear) {
        this.cyear = cyear;
    }

    public String getCcourseday() {
        return ccourseday;
    }

    public void setCcourseday(String ccourseday) {
        this.ccourseday = ccourseday;
    }

    public String getCcoursetime() {
        return ccoursetime;
    }

    public void setCcoursetime(String ccoursetime) {
        this.ccoursetime = ccoursetime;
    }

}
