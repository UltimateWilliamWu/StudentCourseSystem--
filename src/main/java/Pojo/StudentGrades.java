package Pojo;

public class StudentGrades {
    String cname;
    int ggrades,tgrades,fgrades,avg;

    public String getCname() {
        return cname;
    }

    public void setCname(String cname) {
        this.cname = cname;
    }

    public float getGgrades() {
        return ggrades;
    }

    public void setGgrades(int ggrades) {
        this.ggrades = ggrades;
    }

    public int getTgrades() {
        return tgrades;
    }

    public void setTgrades(int tgrades) {
        this.tgrades = tgrades;
    }

    public int getFgrades() {
        return fgrades;
    }

    public void setFgrades(int fgrades) {
        this.fgrades = fgrades;
    }

    public int getAvg() {
        return avg;
    }

    public void setAvg(int avg) {
        this.avg = avg;
    }
}
